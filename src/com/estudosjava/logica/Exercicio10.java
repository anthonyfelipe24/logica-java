package com.estudosjava.logica;

public class Exercicio10 {
    /*🥈 Exercício 2 — WHILE
    Crie um programa que:
    ✅ Comece com:
    int numero = 1;
    ✅ Use while
    ✅ Mostre os números de 1 até 10*/
    public static void main(String[] args) {
        int numero = 1;
        while ( numero <= 10) {
            System.out.println(numero);
           numero++;
        }
    }
}
